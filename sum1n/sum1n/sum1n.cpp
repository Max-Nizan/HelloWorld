#include <stdio.h>
#include <locale.h>

int main()
{
	setlocale(LC_ALL, "RUS");

	int i, n, s=0;

	printf("Введите целое число: ");
	scanf_s("%d", &n);
	for ( i = 1; i <= n; i++)
	{
		s = s + i;
	}
	printf("Сумма первых %d чисел = %d\n", n, s);
}

